#ifndef LIBFT_BITSIZE_H
# define LIBFT_BITSIZE_H

/**
 * \typedef	t_u8
 * \brief	Custom definition of unsigned char data type.
 */
typedef unsigned char	t_u8;
/**
 * \typedef	t_u16
 * \brief	Custom definition of unsigned short data type.
 */
typedef unsigned short	t_u16;
/**
 * \typedef	t_u32
 * \brief	Custom definition of unsigned int data type.
 */
typedef unsigned int	t_u32;
/**
 * \typedef	t_u64
 * \brief	Custom definition of unsigned long data type.
 */
typedef unsigned long	t_u64;
/**
 * \typedef	t_u128
 * \brief	Custom definition of unsigned long long data type.
 */
typedef __uint128_t		t_u128;
/**
 * \typedef	t_s8
 * \brief	Custom definition of signed char data type.
 */
typedef char			t_s8;
/**
 * \typedef	t_s16
 * \brief	Custom definition of signed short data type.
 */
typedef short			t_s16;
/**
 * \typedef	t_s32
 * \brief	Custom definition of signed int data type.
 */
typedef int				t_s32;
/**
 * \typedef	t_s64
 * \brief	Custom definition of signed long data type.
 */
typedef long			t_s64;
/**
 * \typedef	t_s128
 * \brief	Custom definition of signed long long data type.
 */
typedef __int128_t		t_s128;
/**
 * \typedef	t_f32
 * \brief	Custom definition of float data type.
 */
typedef float			t_f32;
/**
 * \typedef	t_f64
 * \brief	Custom definition of double data type.
 */
typedef double			t_f64;
/**
 * \typedef	t_f128
 * \brief	Custom definition of long double data type.
 */
typedef long double		t_f128;

#endif
