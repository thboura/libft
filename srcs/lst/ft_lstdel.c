#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*nxt;
	t_list	*cur;

	if (alst == NULL || del == NULL)
		return ;
	cur = *alst;
	while (cur != NULL)
	{
		nxt = cur->next;
		ft_lstdelone(&cur, del);
		cur = nxt;
	}
	*alst = NULL;
}
