#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*cur;

	cur = lst;
	while (cur != NULL && f != NULL)
	{
		f(cur);
		cur = cur->next;
	}
}
