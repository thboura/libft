#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*tmp_s;
	size_t			i;

	i = 0;
	tmp_s = (unsigned char *)s;
	while (n > i)
	{
		if (tmp_s[i] == (unsigned char)c)
			return ((void *)tmp_s + i);
		i++;
	}
	return (NULL);
}
